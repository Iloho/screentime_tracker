        fontWeight: FontWeight.w500,
        color: textDisabled,
        letterSpacing: 0.5,
        height: 1.45,
      ),
    );
  }

  /// Helper method to get data display text style using JetBrains Mono
  /// For numerical data and technical information display
  static TextStyle getDataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor = isLight ? textPrimaryLight : textPrimaryDark;
    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0,
      height: 1.4,
    );
  }

  /// Helper method to get caption text style for data labels and timestamps
  static TextStyle getCaptionTextStyle({
    required bool isLight,
    double fontSize = 12,
  }) {
    final Color textColor = isLight ? textSecondaryLight : textSecondaryDark;
    return GoogleFonts.inter(
      fontSize: fontSize,
      fontWeight: FontWeight.w400,
      color: textColor,
      letterSpacing: 0.4,
      height: 1.33,
    );
  }
